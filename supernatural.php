				<h3>Avis Personnel</h3>
				<p>
					Supernatural peut, au premier abord, faire peur au public. En lisant le synopsis, nous pouvons penser que la série a une trame principale qui sent le réchauffer et le "déjà-vu". En effet, les légendes urbaines et les créatures de la nuit sont omniprésentes, ainsi que l'interminable combat entre le Ciel et les Enfer. Mais, en réalité, quand nous regardons la série nous sommes surpris, car ce n'est pas du tout à quoi nous nous attendons. C'est pourquoi, les curieux peuvent se poser cette question :
				</p>
				<p>

					POURQUOI REGARDER SUPERNATURAL ?<br><br>
				</p>
				<p>
					Premièrement, LES ACTEURS sont vraiment impressionnants. Je dois dire que je suis extrêmement bluffée par le jeu d'acteur de Jensen Ackles (Dean Winchester) et Jared Padalescki (Sam Winchester). Bien que ces deux-là aient un physique avantageux, nous pouvons penser qu'ils sont simplement là pour attirer l'agente féminine et c'est là que vous vous tromperez. La relation fraternelle est jouée à la perfection. De plus, ils réussissent, en un seul regard, à vous transmettre une émotion. Ils connaissent leur métier, et ça fait du bien de voir à Hollywood des acteurs humbles et modestes, qui devraient être vantards et cons à cause de leurs talents. Ils méritent des oscars. Je dois aussi avouer que des personnages tels que Bobby, Castiel, Crowley, Baltazar, Gabriel, Lucifer, Rubby... Sont également des acteurs doués et charismatiques. Et cette série en est remplie.<br><br>
				</p>
				<p>
					Deuxièmement, L'INTRIGUE.
					Deux frères. Sam, un brillant élève de Stanford, et Dean, qui préfère consacrer sa vie à chasser des créatures surnaturelles avec leur père. Tout les oppose, et ils sont très distants. Mais un soir Dean va trouver Sam pour lui apprendre que "papa est parti chassé, Sam... Et il n'est pas rentré depuis des jours."
					Voilà comment l'histoire commence, Sam et Dean partent à la recherche de leur père et tue des créatures qui croisent leur chemin. Le pilote est un des meilleurs pilotes que je n’ai jamais vus dans ma vie. C'est simple : la trame est clairement expliquée, et les attentes du public sont comblées : action, amour, histoire réfléchie et une pointe d'humour. Comme le reste des épisodes.
					Le créateur de la série, Eric Cripe a fait un excellent travail le long de ces 5 premières saisons. Elles sont appliquées et soignées, je n'ai pas trouvé un seul reproche à faire. Bien que certains soient déçus par la saisons 4 et 5 qui tournent autour de l'apocalypse et du combat entre Ciel et Enfer, je pense que cela a donné un coup de fouet à la série pour qu'elle ne tombe pas dans le schéma classique des séries policières (où chaque épisode représente une seule affaire sans réelle histoire autour). De plus, le sujet de l'apocalypse est très bien traité, et ne tombe pas comme un cheveu sur la soupe. Ensuite, Éric a passé le flambeau. Les premiers épisodes de la saison 6 ont eu du mal à démarrer, car les scénaristes n'avaient pas confiance en eux. Mais au fur et à mesure, la qualité irréprochable de la série est revenue au gallot.
					Les 	saisons 6, 7 et 8 comportent des épisodes justes excellentissimes (comme le reste des saisons), mais elles perdent un peu de la magie que la série avait créé dans le passé. La touche de Cripe manque, mais rien de grave. La saison 8 a tout déchiré, et la 9 promet de grandes choses.<br><br>
				</p>
				<p>
					Troisièmement, l'UNIVERS.
					Les légendes urbaines ou créatures surnaturelles que traitent Supernatural sont un subtil mélange de toutes les rumeurs et de la touche personnelles des scénaristes. Si nous avons l'impression de connaitre une histoire ou une légende, la série la remet au gout du jour. Plus moderne, plus attrayante. L'univers de cette série est unique, bien que beaucoup la comparent à X-File ou autres séries fantastiques. Elle a réussi à créer ses propres symboles (la Chevrolet Impala de 67, le Colt, l'Amulette de Dean...), ses propres répliques cultes (le fameux "Son of a bit*h !" de Dean, ou le "Hey ! As* but !" de Cass, "Idjit !" de Bobby...), et à se trouver un fandom puissant (grâce à lui, la série a remporté de nombreuses récompenses à People Choice Awards notament, et selon les rumeurs, la série continuerait grâce aux nombreux produits dérivés achetés plutôt que grâce aux audiances...).

					Enfin, cette série n'est pas destinée qu'aux adolescents ! Cette série peut très bien plaire aux jeunes adultes tout comme aux personnes âgées. Supernatural parle de sujet puissant et touchant. Ce n'est pas que de simples histoires effrayantes de fantômes ou de démons ! La famille est une valeur omniprésente, l'amour fraternel, la stratégie etc...
				</p>
